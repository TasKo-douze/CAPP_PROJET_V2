CREATE TABLE HS_RESERVATION(
RES_ID NUMBER(6)
  CONSTRAINT PK_HS_RESERVATION PRIMARY KEY,
VIL_ID_DE NUMBER(4)
  CONSTRAINT   FK_VILLE_DE_RESERVATOIN REFERENCES HS_VILLE(VIL_ID),
VIL_ID_POUR NUMBER(4)
  CONSTRAINT   FK_VILLE_POUR_RESERVATOIN REFERENCES HS_VILLE(VIL_ID),
CLI_RES_ID NUMBER(8)
  CONSTRAINT   FK_CLIENT_RESERVATOIN REFERENCES HS_CLIENT(CLI_ID),
RES_DATE_RESERVATION DATE,
RES_DUREEESTIME NUMBER(10),
RES_ACCOMPTE NUMBER(5),
RES_STATUT VARCHAR2(50),
RES_NOMBRE_PASSAGERS_ESTIMEE NUMBER(2) --07
  CONSTRAINT CK_RES_NOMBRE_PASSAGERS_ESTIMEE CHECK (RES_NOMBRE_PASSAGERS_ESTIMEE > 0)
);




