CREATE TABLE HS_ADRESSE(
ADR_ID NUMBER(4)
  CONSTRAINT   PK_HS_ADRESSE PRIMARY KEY ,
ADR_RUE  VARCHAR2(50),
ADR_VILLE VARCHAR2(50),
ADR_NUM NUMBER(4),
ADR_PAYS VARCHAR2(50)
  CONSTRAINT CK_ADR_PAYS CHECK (ADR_PAYS LIKE 'Suisse') --01
);


-----

CREATE TABLE HS_PILOTE(
PILO_ID NUMBER(8)
  CONSTRAINT PK_HS_PILOTE PRIMARY KEY,
PILO_ADR_ID NUMBER(4)
  CONSTRAINT   FK_HS_ADRESSE REFERENCES HS_ADRESSE(ADR_ID),
PILO_NOM VARCHAR2(50)
   CONSTRAINT NN_PILO_NOM NOT NULL,
PILO_PRENOM VARCHAR2(50)
   CONSTRAINT NN_PILO_RPENOM NOT NULL,
PILO_TEL VARCHAR2(17)
   CONSTRAINT NN_PILO_TEL NOT NULL
      CONSTRAINT CK__PILO_TEL CHECK (PILO_TEL LIKE '+41.__.___.__.__'), --02
PILO_EMAIL VARCHAR2(50)
   CONSTRAINT NN_PILO_EMAIL NOT NULL
   CONSTRAINT CK__PILO_EMAIL CHECK (PILO_EMAIL LIKE '%@%.%') --03
);

----
CREATE TABLE HS_HELICOPTERE(
HELI_ID NUMBER(4)
  CONSTRAINT PK_HS_HELICOPTERE PRIMARY KEY, --11
PILO_HELI_ID NUMBER(8)
  CONSTRAINT FK_HS_PILO_HELI REFERENCES HS_PILOTE(PILO_ID), --11
HELI_PLAQUE VARCHAR2(30),
HELI_MODELE VARCHAR2(50),
HELI_CAPACITEMAX NUMBER(2)
   CONSTRAINT NN_HELI_CAPACITEMAX NOT NULL,
HELI_POIDSMAX NUMBER(4)
   CONSTRAINT NN_HELI_POIDSMAX NOT NULL
);


----
CREATE TABLE HS_PASSAGERS(
PASS_ID NUMBER(8)
  CONSTRAINT PK_HS_PASSAGERS PRIMARY KEY,
PASS_TAILLE NUMBER(4)
  CONSTRAINT NN_PASS_TAILLE NOT NULL
  CONSTRAINT CK_PASS_TAILLE CHECK (PASS_TAILLE <= 200), --04
PASS_POIDS NUMBER(4)
  CONSTRAINT NN_PASS_POIDS NOT NULL
  CONSTRAINT CK_PASS_POIDS CHECK (PASS_POIDS <= 100), --05
PASS_POIDSBAGAGE NUMBER(4)
  CONSTRAINT NN_PASS_POIDSBAGAGE NOT NULL
  CONSTRAINT CK_PASS_POIDSBAGAGE CHECK (PASS_POIDSBAGAGE <= 28),  --06
PASS_DATE_NAISSANCE DATE,
PASS_NOM VARCHAR2(50)
  CONSTRAINT NN_PASS_NOM NOT NULL, 
PASS_PRENOM VARCHAR2(50)
  CONSTRAINT NN_PASS_PRENOM NOT NULL,
);



-----
CREATE TABLE HS_CLIENT(

CLI_ID NUMBER(8)
  CONSTRAINT PK_HS_CLIENT PRIMARY KEY,
CLI_ADR_ID NUMBER(4)
  CONSTRAINT   FK_CLI_ADR REFERENCES HS_ADRESSE(ADR_ID),
CLI_DATEINSCRIPTION DATE,
CLI_ACTIVE VARCHAR2(50),
CLI_NOM VARCHAR2(50)
  CONSTRAINT NN_CLI_NOM NOT NULL,
CLI_PRENOM VARCHAR2(50)
  CONSTRAINT NN_CLI_PRENOM NOT NULL,
CLI_TEL VARCHAR2(17)
  CONSTRAINT NN_CLI_TEL NOT NULL
  CONSTRAINT CK__CLI_TEL CHECK (CLI_TEL LIKE '+41.__.___.__.__'), --02
CLI_EMAIL VARCHAR2(50)
  CONSTRAINT NN_CLI_EMAIL NOT NULL
  CONSTRAINT CK__CLI_EMAIL CHECK (CLI_EMAIL LIKE '%@%.%') --03
CLI_MOTS_DE_PASSE VARCHAR2(50)
  CONSTRAINT CLI_MOTS_DE_PASSE NOT NULL 
);



